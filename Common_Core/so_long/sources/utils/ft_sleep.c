void    ft_sleep(unsigned int seconds)
{
    unsigned int    i;
    unsigned int    j;

    i = 0;
    j = 0;
    while (i < seconds)
    {
        while (j < 100000000)
            j++;
        j = 0;
        i++;
    }
    
}